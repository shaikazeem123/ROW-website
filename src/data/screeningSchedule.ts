/**
 * Get events for a specific date
 */
export function getEventsByDate(date: string): ScreeningEvent[] {
    return screeningSchedule.filter(event => event.date === date);
}

/**
 * Get events for a specific month and year
 */
export function getEventsByMonth(year: number, month: number): ScreeningEvent[] {
    const monthStr = month.toString().padStart(2, '0');
    const prefix = `${year}-${monthStr}`;
    return screeningSchedule.filter(event => event.date.startsWith(prefix));
}

/**
 * Check if a date is a screening assessment day (first 5 days of month with screening events)
 */
export function isScreeningDay(date: Date): boolean {
    const day = date.getDate();
    if (day < 1 || day > 5) return false;

    const dateStr = date.toISOString().split('T')[0];
    const events = getEventsByDate(dateStr);
    return events.some(event => event.eventType === 'screening');
}
